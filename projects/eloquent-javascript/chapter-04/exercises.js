function range(start, end, step) {
  let output = [];
  if(step === undefined){
     step = 1
  }else if(step < 0){
    return output
  }
  if(start < end){
    for(let i = start; i <= end; i += step){
      output.push(i)
    }
  }else{
    for(let i = start; i >= end; i -= step){
      output.push(i)
    }
  }
    if(start === end){
      return output = []
    }else{
      return output;
  }
  
}
