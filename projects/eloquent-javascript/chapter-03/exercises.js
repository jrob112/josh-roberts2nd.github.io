// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  if(x < y){
    return x;
  }else if(x>y){
    return y;
  }else{
    return 0
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
// base
if(num === 0){
  return true;
}else if(num === 1){
  return false;
}
// reccursion
if(num > 0){
return isEven(num-2)
}else{
  return isEven(num+2)
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char, count=0) {
// base
if(string.length === 0){
  return count;
}else if(string[0] === char){
  count++
}
// recursion
return countChars(string.slice(1), char, count)
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string, count=0) {
  
// base
if(string.length === 0){
  return count;
}else if(string[0] === 'B'){
  count++
}
// recursion
return countBs(string.slice(1), count)
}

