function drawChessboard(num) {
  let board = "";

  for (var i = 0; i < num; i++) {//width
     if(i>0){
       board += "\n";
      }
     for (var j = 0; j < num; j++) {//height
         if ((j + i) % 2 == 0){
             board += " ";
         } else {
             board += "#";
         }
     }
  }console.log(board + "\n")
 }
